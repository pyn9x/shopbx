CREATE TABLE up_date
(
	ID int not null auto_increment,
	DATE_TRAVEL datetime,
	ACTIVE bool,
	DATE_CREATE timestamp,
	DATE_UPDATE timestamp,

	PRIMARY KEY (ID)
);
CREATE TABLE up_product_date
(
	PRODUCT_ID int not null,
	DATE_ID int not null,
	PRIMARY KEY (PRODUCT_ID, DATE_ID),
	FOREIGN KEY FK_PD_PRODUCT (PRODUCT_ID)
		REFERENCES up_product(ID)
		ON UPDATE RESTRICT
		ON DELETE RESTRICT,
	FOREIGN KEY FK_PD_DATE (DATE_ID)
		REFERENCES up_date(ID)
		ON UPDATE RESTRICT
		ON DELETE RESTRICT
);
DROP TABLE `up_order`;
CREATE TABLE up_order
(
	ID int not null auto_increment,
	FIO varchar(500) not null,
	EMAIL varchar(500) not null,
	PHONE varchar(500) not null,
	DATE_ORDER datetime,
	COMMENT varchar(500) not null,
	STATUS_ID  int not null,
	DATE_ID int not null,
	DATE_CREATE timestamp,
	DATE_UPDATE timestamp,

	FOREIGN KEY FK_ORDER_DATE (DATE_ID)
		REFERENCES up_date(ID)
		ON UPDATE RESTRICT
		ON DELETE RESTRICT,

	FOREIGN KEY FK_ORDER_STATUS (STATUS_ID)
		REFERENCES up_status_order(ID)
		ON UPDATE RESTRICT
		ON DELETE RESTRICT,

	PRIMARY KEY (ID)
);
